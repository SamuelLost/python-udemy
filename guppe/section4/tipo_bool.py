"""
Tipo Booleano

Álgebra Booleana, criada por Geoger Boole

2 constantes, Verdadeiro ou Falso
True -> Verdadeiro
False -> Falso
Sempre com a inicial maiúsculo
"""

ativo = True
print(ativo)

"""
Operações básicas:
"""
# Negação(not):
ativo2 = not ativo
print(ativo)

# Ou (or):
"""
É uma operação binária, ou seja, depende de dois valores.
"""
result = ativo or ativo2

# E(and)
result2 = ativo and ativo2
